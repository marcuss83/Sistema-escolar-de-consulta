    public void carregaTabela(){
        DefaultTableModel tabela = (DefaultTableModel) tabelacoord.getModel(); 
        String nome;
        String cpf;
        String endereco;
        String codigo;
        double salario;
        String curso;
        
        
        for(int i = 0; i < Escola.listaCordenador.size(); i++){        
            nome = Escola.listaCordenador.get(i).nome;
            cpf = Escola.listaCordenador.get(i).cpf;
            endereco = Escola.listaCordenador.get(i).endereco;
            codigo = Escola.listaCordenador.get(i).codigo;
            salario = Escola.listaCordenador.get(i).salario;
            curso = Escola.listaCordenador.get(i).curso;
            Object[] dado = {codigo,nome, cpf, endereco, salario, curso};
            tabela.addRow(dado);
            
        }
    }

